package ch02.sec02;

public class IntergerLiteralExample {
    public static void main(String[] args) {
        int var1 = 0b1011; // 2진수 : ob를 접두사로
        int var2 = 0206;
        int var3 = 365;
        int var4 = 0xB3;

        System.out.printf("var1: " + var1);
        System.out.printf("var2: " + var2);
        System.out.printf("var3: " + var3);
        System.out.printf("var4: " + var4);
    }
}
